"""
    Conversão de tipos, coerção
    type convertion, typecastring, coercion é o ato de converter um tipo em outro.
    Tipos imutaveis e primitivos: str, int, float, bool 
"""

#Para converter uma string em inteiro basta utilizar a função int(), float(), str() ou bool()
print(int('1') + 1)
print(float('1') + 1)
print(str(2) + 'a')
print(bool('f'))